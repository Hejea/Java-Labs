
public class ComplexNumber {
	
	private float re;
	private float im;

	public ComplexNumber() {
		super();
	}
	
	public ComplexNumber(float re, float im) {
		super();
		this.re = re;
		this.im = im;
	}

	public float getRe() {
		return re;
	}

	public void setRe(float re) {
		this.re = re;
	}

	public float getIm() {
		return im;
	}

	public void setIm(float im) {
		this.im = im;
	}

}
